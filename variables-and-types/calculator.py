def add(first, second):
    ans = first + second
    return ans

def sub(first, second):
    ans = first - second
    return ans

def multiply(first, second):
    ans = first * second
    return ans

def div_with_floor(first, second):
    ans = first // second
    return ans


def div_without_floor(first, second):
    ans = first / second
    return ans


